#include<bits/stdc++.h>

using namespace std;


int main()
{
    int T;
    string S;
    scanf("%d",&T);

    for(int i=1;i<=T;i++)
    {
        cin>>S;

        for(int j=0;j<S.size();i++)
        {
            for(int k=0;k<j;k++)
            {
                cout<<S[k]<<"\n";
            }
        }
        cout<<endl;
    }
}
